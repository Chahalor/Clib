#define MIN(array, T, size)						\
({												\
	T __array_min = ((T *)(array))[0];			\
	for (size_t __i = 1; __i < (size); ++__i)	\
	{											\
		if (((T *)(array))[__i] < __array_min)	\
			__array_min = ((T *)(array))[__i];	\
	}											\
	__array_min;								\
})

/* ************************************************************************** */
/*                                 Inlines                                    */
/* ************************************************************************** */

static inline int	min(
	const int _val1,
	const int _val2
)
{
	return (_val1 < _val2
			? _val1
			: _val2
		);
}

static inline int	max(
	const int _val1,
	const int _val2
)
{
	return (_val1 > _val2
			? _val1
			: _val2
		);
}

