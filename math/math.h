static inline int	min(
	const int _val1,
	const int _val2
)
{
	return (_val1 < _val2
			? _val1
			: _val2
		);
}

static inline int	max(
	const int _val1,
	const int _val2
)
{
	return (_val1 > _val2
			? _val1
			: _val2
		);
}

